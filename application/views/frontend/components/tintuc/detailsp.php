		function saveLienHe(){
			var strurl = "<?php echo base_url(); ?>" + 'lienhe/insertCmt';
			var name = $("#name").val();
			var email = $("#email").val();
			var phone = $("#phone").val();
			var description = $("#description").val();
			$(".contact_btn_1 i").removeClass('d-none');
			var validate = false;
			if (name === "" ) {
				$("#name").focus();
			} else if (phone == "") {
				$("#phone").focus();
			}else if (description == "") {
				$("#description").focus();
			}else{
				validate = true;
			}
			if (validate) {
				jQuery.ajax({
					url: strurl,
					type: 'POST',
					dataType: 'json',
					data: {
						name:name,
						phone:phone,
						email:email,
						description:description
					},
					success: function(data) {
						// window.location.reload(true);
						$(".text-success").show();
						$("#name").val("");
						$("#phone").val("");
						$("#email").val("");
						$("#description").val("");
						$(".contact_btn_1 i").addClass('d-none');
						setTimeout(function() { $(".text-success").hide(); }, 3000);
					}
				});
			}
		}
	</script>
